package Com.Syntax.class04;

public class NestedForEva {
	public static void main(String[] args) {
		
		int sum=5;
		for ( int x=0; x<=10; x++) {
			sum=sum+x;
			// sum= 0+0..0
			//sum=0+1 ..1
			//sum =1+2 ...3
			// sum = 3+3...6
			// sum = 6+4...10
			//sum= 10+5 ...15
			
			System.out.println(sum);
		}
	}}
